    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre para el solicitante");
            txtnombre.requestFocus();
            return;
        }
        if(!txtnombre.getText().matches("[a-zA-Z]*")){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar caracteres validos en el Nombre  para el solicitante");
            txtnombre.requestFocus();
            return;
        }
        if (txtapaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido para el solicitante");
            txtapaterno.requestFocus();
            return;
        }
        if(!txtapaterno.getText().matches("[a-zA-Z]*")){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar caracteres validos en el apellido  para el solicitante");
            txtapaterno.requestFocus();
            return;
        }
        
        if (txtamaterno.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido para el solicitante");
            txtamaterno.requestFocus();
            return;
        }
        if(!txtamaterno.getText().matches("[a-zA-Z]*")){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar caracteres validos en el apellido  para el solicitante");
            txtamaterno.requestFocus();
            return;
        }

        if (txtnum_documento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Número de Doc para el solicitante");
            txtnum_documento.requestFocus();
            return;
        }
        if(!txtnum_documento.getText().matches("[0-9]*")){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Número de Doc para el solicitante");
            txtnum_documento.requestFocus();
            return;
        }
        
        if (txtcodigo_solicitante.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Código para el solicitante");
            txtcodigo_solicitante.requestFocus();
            return;
        }
        
        if(!txtcodigo_solicitante.getText().matches("[0-9]*")){
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Número de Doc para el solicitante");
            txtcodigo_solicitante.requestFocus();
            return;
        }

        Csolicitante dts = new Csolicitante();
        fsolicitante func = new fsolicitante();

        dts.setNombre(txtnombre.getText());

        dts.setApaterno(txtapaterno.getText());
        dts.setAmaterno(txtamaterno.getText());
        
        int seleccionado = cbotipo_documento.getSelectedIndex();
        dts.setTipo_documento((String) cbotipo_documento.getItemAt(seleccionado));
        dts.setNum_documento(txtnum_documento.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setCodigo_solicitante(txtcodigo_solicitante.getText());
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "el solicitante fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        }
        else if (accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El solicitante fue Editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion="editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtidpersona.setText(tablalistado.getValueAt(fila, 0).toString());
        txtnombre.setText(tablalistado.getValueAt(fila, 1).toString());

        txtapaterno.setText(tablalistado.getValueAt(fila, 2).toString());
        txtamaterno.setText(tablalistado.getValueAt(fila, 3).toString());
        cbotipo_documento.setSelectedItem(tablalistado.getValueAt(fila, 4).toString());
        txtnum_documento.setText(tablalistado.getValueAt(fila, 5).toString());
        txtdireccion.setText(tablalistado.getValueAt(fila, 6).toString());
        txttelefono.setText(tablalistado.getValueAt(fila, 7).toString());
        txtemail.setText(tablalistado.getValueAt(fila, 8).toString());
        txtcodigo_solicitante.setText(tablalistado.getValueAt(fila, 9).toString());

    }//GEN-LAST:event_tablalistadoMouseClicked

