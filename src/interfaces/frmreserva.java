    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidreserva.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Est√°s seguro de Eliminar la Reserva?","Confirmar",2);

            if (confirmacion==0) {
                freserva func = new freserva ();
                Creserva dts= new Creserva();
                
                faula func3= new faula();
                Caula dts3 = new Caula();
                
                dts3.setIdaula(Integer.parseInt(txtidaula.getText()));
                func3.desocupar(dts3);

                dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
                func.eliminar(dts);
                mostrar("");
                
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

