    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        //System.out.println(txtidaula.getText());
        if (txtidaula.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Seleccionar un Aula");
            txtidaula.requestFocus();
            return;
        }
        if (txtidsolicitante.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Seleccionar un Solicitante");
            txtidsolicitante.requestFocus();
            return;
        }


        Creserva dts = new Creserva();
        freserva func = new freserva();
        
        dts.setIdaula(Integer.parseInt(txtidaula.getText()));
        dts.setIdsolicitante(Integer.parseInt(txtidsolicitante.getText()));
        dts.setIdtrabajador(idusuario);
        

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue registrada satisfactoriamente");
                mostrar("");
                
                
                //ocupamos aula 
                faula func3= new faula();
                Caula dts3 = new Caula();
                
                dts3.setIdaula(Integer.parseInt(txtidaula.getText()));
                func3.ocupar(dts3);
                inhabilitar();
            }

        }
        else if (accion.equals("editar")){
            dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
            dts.setIdtrabajador(Integer.parseInt(txtidtrabajador.getText()));            
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva fue Editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

